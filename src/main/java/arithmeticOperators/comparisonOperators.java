package arithmeticOperators;

public class comparisonOperators {
    public static void main(String[] args) {

        /*
        Comparison operators are used to compare 2 values:
        > more than
        < less than
        >= more or equal
        <= less or equal
        == equal
        != not equal


         */
        byte potato = 5;
        byte fries = 10;
        System.out.println(potato > fries); // false
        System.out.println(potato < fries); //true


        byte apple = 4;
        byte appleChips = 6;

        System.out.println(apple > appleChips);

        short banana = 54;
        short pineapple = 100;

        int tomato = 12345;
        int apricot = 23455;

        long pen = 300000000000000L;
        long pencil = 4444444444444L;







    }
}
