package loops;

public class NestedLoops {
    public static void main(String[] args) {

        /*
        nested loop - loop inside loop
         */

        for (int m = 1; m <= 15; m++) {
            for (int k = 1; k <= 15; k++) {
                System.out.print((m * k) + "\t");
                }
                System.out.println();
            }






        for( int x = 1; x <= 10; x++){
            for ( int y = 1; y <=x; y++){
                System.out.print("* ");
            }
            System.out.println();
        }






















        }
    }
